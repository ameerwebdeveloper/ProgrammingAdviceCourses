
#include <iostream>
using namespace std;

int main() {

	// Relational Operators

	int A = 10, B = 20;

	cout << "A -> " << A << " B --> " << B << endl;
	cout << "A == B " << (A == B) << endl;
	cout << "A != B " << (A != B) << endl;
	cout << "A > B " << (A > B) << endl;
	cout << "A >= B " << (A >= B) << endl;
	cout << "A < B " << (A < B) << endl;
	cout << "A <= B " << (A <= B) << endl;


	return 0;
}

