
#include <iostream>
using namespace std;

int main() {

	// Operators

	int A = 1, B = 0;
	cout << "A " << A << " B " << B << endl;
	cout << "!A " << !A << " !B " << !B << endl;
	cout << " A && B " << (A && B) << endl;
	cout << " A || B " << (A || B) << endl;
	cout << " !(A || B) " << !(A || B) << endl;
	cout << " !(A && B) " << !(A && B) << endl;

	return 0;
}

